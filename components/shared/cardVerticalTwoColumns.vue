<template>
    <v-card>
    <v-card-title class="align-start">
        <v-avatar
            :color="color"
            size="38"
            class="elevation-3"
        >
            <v-icon
            size="24"
            color="white"
            class="rounded-0"
            >
            {{ icon }}
            </v-icon>
        </v-avatar>
        <v-spacer></v-spacer>
        <v-btn
            small
            icon
            class="me-n3 mt-1"
        >
            <v-icon>mdi-dots-vertical</v-icon>
        </v-btn>
    </v-card-title>

    <v-card-text class="text-no-wrap text--primary mt-6">
        <p class="font-weight-semibold text-sm mb-1">
            {{ statTitle }}
        </p>

        <div class="d-flex align-end flex-wrap">
            <span class="font-weight-bold text-h5 me-1 mb-2">{{ statistics }}</span>
            <span
            class="percentage text-xs mb-2"
            :class="checkChange(change) ? 'success--text':'error--text'"
            > {{ change }}</span>
        </div>
        <p class="text-xs text--secondary mb-0">
            {{ subtitle }}
        </p>
    </v-card-text>
  </v-card>
</template>

<script>
export default {
    props: {
        statTitle:  { type: String, default: '' },
        icon:       { type: String, default: '' },
        color:      { type: String, default: '' },
        subtitle:   { type: String, default: '' },
        statistics: { type: String, default: '' },
        change:     { type: String, default: '' },
    },
    methods: {
        checkChange(value) {
            const firstChar = value.charAt(0)
            if (firstChar === '+') {
                return true
            }

            return false
        }
    }
}
</script>

